<h1>Media Help Needed</h1>

<p>
  Our BIGGEST need right now is getting the information out to all the local
  healthcare organizations that we have a solution on the way. This includes
  doctors and dentists offices, EMS and first responders, hospice and home
  health groups etc.
</p>
<p>
  Please click on the following links and share all the neat things that
  Make-A-Mask is doing to help support our local community.
</p>

<p>
  You can
  <strong>COPY AND PASTE</strong>
  this paragraph to any of the links below.
</p>

<p>
  Jennifer Reyes, Director of Clinical Operations at Victory Medical, was
  struggling with the lack of PPE within her clinics and turned to Austin and
  the surrounding community via social media for assistance in sewing masks for
  healthcare workers. Make-A-Mask was created on Facebook as a “Call to action
  for all local sewers” late Friday evening and quickly went viral with
  #MakeAMaskSewers. The post has already reached 20,000 people and it is
  believed that tens of thousands of masks are being prepared and on their way
  to the Austin community from all over the USA. The media is needed to help get
  the word out to local healthcare organizations on how to register for the sewn
  mask distribution. The sewn masks will be worn OVER their surgical and N95
  masks but only the sewn masks will be changed out in between each patient,
  prolonging the life of their medical grade masks. Please reference the
  following link for more information on the Make-A-Mask program:
  <a href="/#faq">Make A Mask FAQ page</a>
</p>

